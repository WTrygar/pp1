x = int(input('podaj ocenę numerycznie: '))

if x == 1:
    print("niedostateczny")
elif x == 2:
    print("mierny")
elif x == 3:
    print("dostateczny")
elif x == 4:
    print("dobry")
elif x == 5:
    print("bardzo dobry")
elif x == 6:
    print("celujący")
else:
    print("nie ma takiej oceny")
